package com.sudhan;

public class IfElseLadder {
	public static void main(String[] args) {
		int n=20;
		if(n<=5) {
			System.out.println("condition1 is correct");
		}else if(n<=10) {
			System.out.println("condition2 is correct");
		}else if(n<=20) {
			System.out.println("condition3 is correct");
		}else {
			System.out.println("no condition is correct");
		}
	}

}
